void Switch_Case() {
  String fruit = 'apple';

  switch (fruit) {
    case 'apple':
      print('You selected an apple.');
      break;
    case 'banana':
      print('You selected a banana.');
      break;
    case 'orange':
      print('You selected an orange.');
      break;
    default:
      print('Invalid fruit selection.');
  }
}
